package goldsmith.j.a.engine;

public class Inputs {

    private int age;
    private int timeOff;

    public Inputs(int age, int timeOff) {
        this.age = age;
        this.timeOff = timeOff;
    }

    public int getAge() {
        return age;
    }

    public int getTimeOff() {
        return timeOff;
    }

}
